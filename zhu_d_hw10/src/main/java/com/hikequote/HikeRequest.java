// HikeRequest.java
package com.hikequote;

public class HikeRequest {
    private String hike;
    private String date;
    private String duration;
    private String numHikers;

    // Getters and setters
    public String getHike() { return hike; }
    public void setHike(String hike) { this.hike = hike; }
    public String getDate() { return date; }
    public void setDate(String date) { this.date = date; }
    public String getDuration() { return duration; }
    public void setDuration(String duration) { this.duration = duration; }
    public String getNumHikers() { return numHikers; }
    public void setNumHikers(String numHikers) { this.numHikers = numHikers; }
}
